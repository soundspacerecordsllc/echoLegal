// ── Tests: deterministic UID format ──────────────────────────────────

describe('Calendar ICS: deterministic UID', () => {
  it('UID format is fc-{entityId}-{form}-{dueDate}', () => {
    const deadlines = [{ form: 'Form 5472', dueDate: '2025-09-15' }]
    const ics = generateICS(makeInput(deadlines))
    const expected = `fc-${ENTITY_ID}-Form 5472-2025-09-15`
    assert.ok(ics.includes(`UID:${expected}`))
  })

  it('same inputs produce same UIDs', () => {
    const deadlines = [{ form: 'Form 1120-F', dueDate: '2025-10-15' }]
    const ics1 = generateICS(makeInput(deadlines))
    const ics2 = generateICS(makeInput(deadlines))
    assert.equal(ics1, ics2)
  })

  it('different entities produce different UIDs', () => {
    const dl = [{ form: 'Form X', dueDate: '2025-09-15' }]
    const ics1 = generateICS(makeInput(dl, { entityId: 'aaa' }))
    const ics2 = generateICS(makeInput(dl, { entityId: 'bbb' }))
    const events1 = parseVEvents(ics1)
    const events2 = parseVEvents(ics2)
    assert.notEqual(events1[0].UID, events2[0].UID)
  })
})

// ── Tests: sorted ordering ───────────────────────────────────────────

describe('Calendar ICS: sorted ordering', () => {
  it('events are sorted ascending by dueDate', () => {
    const deadlines = [
      { form: 'Form C', dueDate: '2025-12-01' },
      { form: 'Form A', dueDate: '2025-07-01' },
      { form: 'Form B', dueDate: '2025-09-15' },
    ]
    const ics = generateICS(makeInput(deadlines))
    const events = parseVEvents(ics)
    assert.equal(events.length, 3)
    assert.ok(events[0]['DTSTART;VALUE=DATE'] < events[1]['DTSTART;VALUE=DATE'])
    assert.ok(events[1]['DTSTART;VALUE=DATE'] < events[2]['DTSTART;VALUE=DATE'])
  })

  it('events with same dueDate are sorted by UID', () => {
    const deadlines = [
      { form: 'Form Z', dueDate: '2025-09-15' },
      { form: 'Form A', dueDate: '2025-09-15' },
    ]
    const ics = generateICS(makeInput(deadlines))
    const events = parseVEvents(ics)
    assert.equal(events.length, 2)
    assert.ok(events[0].UID < events[1].UID)
  })
})

// ── Tests: past deadline filtering ───────────────────────────────────

describe('Calendar ICS: past deadline filtering', () => {
  it('excludes deadlines older than 30 days', () => {
    const deadlines = [
      { form: 'Old Form', dueDate: '2025-05-01' }, // 45 days before June 15
      { form: 'Recent Form', dueDate: '2025-09-15' },
    ]
    const ics = generateICS(makeInput(deadlines))
    const events = parseVEvents(ics)
    assert.equal(events.length, 1)
    assert.ok(events[0].SUMMARY.includes('Recent Form'))
  })

  it('includes deadlines within 30-day past window', () => {
    const deadlines = [
      { form: 'Recent Past', dueDate: '2025-05-20' }, // 26 days before June 15
      { form: 'Future', dueDate: '2025-09-15' },
    ]
    const ics = generateICS(makeInput(deadlines))
    const events = parseVEvents(ics)
    assert.equal(events.length, 2)
  })

  it('includes deadlines due today', () => {
    const deadlines = [
      { form: 'Today', dueDate: '2025-06-15' },
    ]
    const ics = generateICS(makeInput(deadlines))
    const events = parseVEvents(ics)
    assert.equal(events.length, 1)
  })
})

// ── Tests: empty input ───────────────────────────────────────────────

describe('Calendar ICS: empty input', () => {
  it('empty entities produces valid calendar with no events', () => {
    const ics = generateICS({ entities: [], now: FIXED_NOW, dtstamp: FIXED_STAMP })
    assert.ok(ics.includes('BEGIN:VCALENDAR'))
    assert.ok(ics.includes('END:VCALENDAR'))
    assert.ok(!ics.includes('BEGIN:VEVENT'))
  })

  it('entity with empty deadlines produces no events', () => {
    const ics = generateICS(makeInput([]))
    assert.ok(!ics.includes('BEGIN:VEVENT'))
  })
})

// ── Tests: ensureCalendarToken ───────────────────────────────────────

describe('Calendar: ensureCalendarToken', () => {
  it('generates token when null', () => {
    const result = ensureCalendarToken({ calendar_token: null })
    assert.ok(result.calendar_token)
    assert.equal(result.needsUpdate, true)
  })

  it('generates token when undefined', () => {
    const result = ensureCalendarToken({})
    assert.ok(result.calendar_token)
    assert.equal(result.needsUpdate, true)
  })

  it('preserves existing token', () => {
    const existing = 'existing-token-123'
    const result = ensureCalendarToken({ calendar_token: existing })
    assert.equal(result.calendar_token, existing)
    assert.equal(result.needsUpdate, false)
  })

  it('generated tokens are UUID format', () => {
    const result = ensureCalendarToken({})
    const uuidRegex = /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/
    assert.ok(uuidRegex.test(result.calendar_token))
  })

  it('each generation produces a unique token', () => {
    const r1 = ensureCalendarToken({})
    const r2 = ensureCalendarToken({})
    assert.notEqual(r1.calendar_token, r2.calendar_token)
  })
})

