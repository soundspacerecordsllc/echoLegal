// ── Inline the ICS generator (mirrors lib/filingcontrol/calendar/ics.ts) ──

function formatDTStamp(d) {
  return d.toISOString().replace(/[-:]/g, '').replace(/\.\d{3}/, '')
}

function formatDateValue(iso) {
  return iso.replace(/-/g, '')
}

function buildUID(entityId, form, dueDate) {
  return `fc-${entityId}-${form}-${dueDate}`
}

function escapeText(text) {
  return text
    .replace(/\\/g, '\\\\')
    .replace(/;/g, '\\;')
    .replace(/,/g, '\\,')
    .replace(/\n/g, '\\n')
}

function generateICS(input) {
  const { entities, now, dtstamp } = input
  const stamp = dtstamp ?? formatDTStamp(now)

  const cutoff = new Date(now)
  cutoff.setDate(cutoff.getDate() - 30)
  const cutoffStr = cutoff.toISOString().split('T')[0]

  const events = []

  for (const { entity, deadlines } of entities) {
    for (const dl of deadlines) {
      if (dl.dueDate < cutoffStr) continue

      const daysRemaining = Math.floor(
        (new Date(dl.dueDate).getTime() - now.getTime()) / (1000 * 60 * 60 * 24)
      )

      const descParts = [
        `Entity: ${entity.company_name}`,
        `Due: ${dl.dueDate}`,
      ]
      if (daysRemaining >= 0) {
        descParts.push(`Days remaining: ${daysRemaining}`)
      }
      descParts.push('Generated by FilingControl (EchoLegal)')

      events.push({
        uid: buildUID(entity.id, dl.form, dl.dueDate),
        dtstart: formatDateValue(dl.dueDate),
        summary: `${dl.form} \u2013 Filing Deadline`,
        description: descParts.join('\\n'),
        dueDate: dl.dueDate,
      })
    }
  }

  events.sort((a, b) => {
    if (a.dueDate !== b.dueDate) return a.dueDate < b.dueDate ? -1 : 1
    return a.uid < b.uid ? -1 : a.uid > b.uid ? 1 : 0
  })

  const lines = [
    'BEGIN:VCALENDAR',
    'VERSION:2.0',
    'PRODID:-//EchoLegal//FilingControl//EN',
    'CALSCALE:GREGORIAN',
  ]

  for (const evt of events) {
    lines.push(
      'BEGIN:VEVENT',
      `UID:${escapeText(evt.uid)}`,
      `DTSTAMP:${stamp}`,
      `DTSTART;VALUE=DATE:${evt.dtstart}`,
      `SUMMARY:${escapeText(evt.summary)}`,
      `DESCRIPTION:${escapeText(evt.description)}`,
      'END:VEVENT'
    )
  }

  lines.push('END:VCALENDAR')

  return lines.join('\r\n')
}

// ── Inline ensureCalendarToken (mirrors lib/filingcontrol/calendar/token.ts) ──

function generateCalendarToken() {
  return randomUUID()
}

function ensureCalendarToken(userRow) {
  if (userRow.calendar_token) {
    return { calendar_token: userRow.calendar_token, needsUpdate: false }
  }
  return { calendar_token: generateCalendarToken(), needsUpdate: true }
}

// ── Test helpers ──────────────────────────────────────────────────────

const FIXED_NOW = new Date(2025, 5, 15) // June 15, 2025
const FIXED_STAMP = '20250615T000000Z'
const ENTITY_ID = '00000000-0000-0000-0000-000000000001'
const ENTITY_NAME = 'Test LLC'

function makeInput(deadlines, options = {}) {
  return {
    entities: [{
      entity: {
        id: options.entityId ?? ENTITY_ID,
        company_name: options.entityName ?? ENTITY_NAME,
      },
      deadlines,
    }],
    now: options.now ?? FIXED_NOW,
    dtstamp: options.dtstamp ?? FIXED_STAMP,
  }
}

function parseVEvents(ics) {
  const events = []
  const blocks = ics.split('BEGIN:VEVENT')
  for (let i = 1; i < blocks.length; i++) {
    const block = blocks[i].split('END:VEVENT')[0]
    const lines = block.split('\r\n').filter(Boolean)
    const evt = {}
    for (const line of lines) {
      const colonIdx = line.indexOf(':')
      if (colonIdx > 0) {
        const key = line.substring(0, colonIdx)
        const value = line.substring(colonIdx + 1)
        evt[key] = value
      }
    }
    events.push(evt)
  }
  return events
}

