/**
 * Unit tests: FilingControl ICS calendar feed.
 *
 * Validates:
 *   1. ICS header structure (VCALENDAR, VERSION, PRODID, CALSCALE)
 *   2. VEVENT structure (UID, DTSTAMP, DTSTART, SUMMARY, DESCRIPTION)
 *   3. Deterministic UID format: fc-{entityId}-{form}-{dueDate}
 *   4. Events sorted ascending by dueDate
 *   5. Past deadlines older than 30 days are excluded
 *   6. Empty input produces valid empty calendar
 *   7. Calendar token generation (ensureCalendarToken)
 *   8. Route source checks (token lookup, PRO check, Content-Type)
 *
 * Run: node --test scripts/test-calendar-ics.mjs
 */

